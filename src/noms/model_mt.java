package noms;

public class model_mt {
    private String id_menu, nama_menu, kategori;
    private int harga;

    public String getId_menu() { return id_menu; }
    public void setId_menu(String id_menu) { this.id_menu = id_menu; }

    public String getNama_menu() { return nama_menu; }
    public void setNama_menu(String nama_menu) { this.nama_menu = nama_menu; }

    public int getHarga() { return harga; }
    public void setHarga(int harga) { this.harga = harga; }

    public String getKategori() { return kategori; }
    public void setKategori(String kategori) { this.kategori = kategori; }
}
