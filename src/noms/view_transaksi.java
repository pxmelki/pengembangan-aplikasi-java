    private void btnsimpantransaksiActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnsimpantransaksiActionPerformed
    try {
        model.setTotalHarga(Double.parseDouble(txttotalharga.getText()));
        model.setUangBayar(Double.parseDouble(txtuangbayar.getText()));
        model.setKembalian(Double.parseDouble(txtkembalian.getText()));
    } catch(Exception e) {
        JOptionPane.showMessageDialog(this, "Data pembayaran belum lengkap!");
        return;
    }

    boolean sukses = controller.simpanTransaksi();
    if (sukses) {
        JOptionPane.showMessageDialog(this, "Transaksi berhasil disimpan!");

    } else {
        JOptionPane.showMessageDialog(this, "Gagal menyimpan transaksi!");
    }
    

    }//GEN-LAST:event_btnsimpantransaksiActionPerformed

