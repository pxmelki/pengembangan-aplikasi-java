    private void btnbayarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnbayarActionPerformed
        try {
            double total = Double.parseDouble(txttotalharga.getText());
            double bayar = Double.parseDouble(txtuangbayar.getText());

        if (bayar < total) {
            JOptionPane.showMessageDialog(this, "Uang bayar kurang dari total!");
            return;
        }

        double kembalian = bayar - total;
        txtkembalian.setText(String.valueOf(kembalian));

        model.setUangBayar(bayar);
        model.setKembalian(kembalian);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Input uang tidak valid!");
        }
    }//GEN-LAST:event_btnbayarActionPerformed

    private void btnclearActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnclearActionPerformed
        txtharga.setText("");
        txtjumlah.setText("");
        txtsubtotal.setText("");
        txttotalharga.setText("");
        txtuangbayar.setText("");
        txtkembalian.setText("");

        tableModel.setRowCount(0);

        model.setTotalHarga(0);
        model.setUangBayar(0);
        model.setKembalian(0);
        model.getDaftarItem().clear();

        model.setIdTransaksi(controller.generateIdTransaksi());
        model.setTanggal(java.time.LocalDate.now().toString());

        cmbitem.setSelectedIndex(0);
        txtjumlah.requestFocus();

        JOptionPane.showMessageDialog(this, "Form transaksi telah dikosongkan!");
    }//GEN-LAST:event_btnclearActionPerformed

