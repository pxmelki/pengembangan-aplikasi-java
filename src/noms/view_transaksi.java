    private void btnkeranjangActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnkeranjangActionPerformed
        String nama = (String) cmbitem.getSelectedItem();
        String kodeMenu = controller.getIdMenuByName(nama);
        double harga = Double.parseDouble(txtharga.getText());
        int jumlah = Integer.parseInt(txtjumlah.getText());
        double subtotal = harga * jumlah;

        model_transaksi.model_item item = model.new model_item(kodeMenu, nama, harga, jumlah);
        model.tambahItem(item);
        tableModel.addRow(new Object[]{kodeMenu, nama, jumlah, harga, subtotal});

        double total = model.getTotalHarga() + subtotal;
        model.setTotalHarga(total);
        txttotalharga.setText(String.valueOf(total));

        txtjumlah.setText("");
        txtsubtotal.setText("");
    }//GEN-LAST:event_btnkeranjangActionPerformed

