    private void btnhapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnhapusActionPerformed
        int row = tbmenu.getSelectedRow();
            if(row >= 0){
        String id = tbmenu.getValueAt(row, 0).toString();
        control.delete(id);
        tampilData();
        kosongkan();
            } else {
        JOptionPane.showMessageDialog(this, "Pilih data dulu");
        }
    }//GEN-LAST:event_btnhapusActionPerformed

    private void btntambahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btntambahActionPerformed
        if (txtnamamenu.getText().isEmpty() || txthargamenu.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Nama dan Harga tidak boleh kosong!");
            return;
        }

        try {
            model_mt newModel = new model_mt();
            newModel.setNama_menu(txtnamamenu.getText());
            newModel.setHarga((int) Double.parseDouble(txthargamenu.getText()));
            newModel.setKategori(cbkategorimennu.getSelectedItem().toString());

            control.insert(newModel);

            tampilData();
            kosongkan();

        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Harga harus berupa angka yang valid!");
        }
    }//GEN-LAST:event_btntambahActionPerformed

