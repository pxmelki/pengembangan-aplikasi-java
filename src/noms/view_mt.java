    private void btnubahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnubahActionPerformed
        if (txtnamamenu.getText().isEmpty() || txthargamenu.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Nama dan Harga tidak boleh kosong!");
            return;
        }

        model.setNama_menu(txtnamamenu.getText());
        model.setHarga((int) Double.parseDouble(txthargamenu.getText()));
        model.setKategori(cbkategorimennu.getSelectedItem().toString());

        control.insert(model);
        tampilData();
        kosongkan();
        int row = tbmenu.getSelectedRow();
        if (row >= 0) {
            if (txtnamamenu.getText().isEmpty() || txthargamenu.getText().isEmpty()) {
                JOptionPane.showMessageDialog(this, "Nama menu dan Harga harus diisi!");
                return;
                }

        model.setId_menu(tbmenu.getValueAt(row, 0).toString());
        model.setNama_menu(txtnamamenu.getText());
        model.setHarga((int) Double.parseDouble(txthargamenu.getText()));
        model.setKategori(cbkategorimennu.getSelectedItem().toString());

        control.update(model);
        tampilData();
        kosongkan();
        } else {
            JOptionPane.showMessageDialog(this, "Pilih data dulu di tabel untuk diubah.");
        }

    }//GEN-LAST:event_btnubahActionPerformed

